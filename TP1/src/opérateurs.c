int main() {
    int a = 16;
    int b = 3;

    printf("a = %d, b = %d\n", a, b);
    printf("Addition (a + b) : %d\n", a + b);
    printf("Soustraction (a - b) : %d\n", a - b);
    printf("Multiplication (a * b) : %d\n", a * b);
    printf("Division entiere (a / b) : %d\n", a / b);
    printf("Modulo (a %% b) : %d\n", a % b); // %% pour afficher un %

    // Comparaisons (1 = Vrai, 0 = Faux)
    printf("Egalite (a == b) : %d\n", a == b);
    printf("Superieur (a > b) : %d\n", a > b);

    return 0;
}
